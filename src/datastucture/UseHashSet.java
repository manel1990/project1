package datastucture;

import java.util.HashSet;
import java.util.Set;

public class UseHashSet {
    public static void main(String[] args) {
        Set<Integer> hs=new HashSet<>();
        hs.add(2);
        hs.add(3);
        hs.add(1);
        hs.add(6);
        System.out.println(hs);
    }
}
